export enum SubmissionState {
	/**
	 * Indicates that a submission hasn't been solved
	 */
	Unsolved = 'UNSOLVED',

	/**
	 * Indicates that a submission has been solved
	 */
	Solved = 'SOLVED',

	/**
	 * Indicates that a submission has been completely solved
	 */
	CompletelySolved = 'COMPLETELY_SOLVED',
}
