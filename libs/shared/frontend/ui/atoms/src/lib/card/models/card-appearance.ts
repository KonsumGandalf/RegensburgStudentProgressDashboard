/**
 * Enum representing different appearances for a card component.
 */
export enum CardAppearance {
	/**
	 * Solid appearance for the card.
	 */
	SOLID = 'solid',

	/**
	 * Outlined appearance for the card.
	 */
	OUTLINED = 'outlined',

	/**
	 * Raised appearance for the card.
	 */
	RAISED = 'raised',
}
